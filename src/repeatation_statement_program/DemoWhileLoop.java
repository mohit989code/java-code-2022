package repeatation_statement_program;

public class DemoWhileLoop {

    public static void main(String[] args) {

        int i = 0;

        while (i <= 50) {
            System.out.println("print number increasing order:  \n" + i);
            i++;
        }
//DECREMENT
        int j = 0;

        while (j <= 50) {
            System.out.println("print number decreasing order:  \t" + j);
            j++;
        }
    }
}
