package repeatation_statement_program;

public class DemoOfLoops {

    public static void main(String[] args) {

        for (int i=0; i<10; i++){ //use post increment operator

            System.out.println("I AM PROUD OF MY INDIA: " +i);
        }


        for (int i=10; i>0; i--){ //use post decrement operator

            System.out.println("I AM PROUD OF MY INDIA: " +i);
        }

    }
    }

