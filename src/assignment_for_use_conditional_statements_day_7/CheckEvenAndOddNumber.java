package assignment_for_use_conditional_statements_day_7;

public class CheckEvenAndOddNumber {

    public static void main(String[] args) {

        int n=2;
        if (n!=0){

            if (n%2==0){

                System.out.println("NUMBER IS EVEN:" +n);
            }
            else {

                System.out.println("NUMBER IS ODD:" +n);
            }
        }
    }
}
