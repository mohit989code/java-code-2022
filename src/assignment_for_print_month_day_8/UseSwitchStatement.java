package assignment_for_print_month_day_8;

public class UseSwitchStatement {

    public static void main(String[] args) {

        int MONTH= 11;

        switch (MONTH){

            case 1:
                System.out.println("YEAR OF THE MONTH JANUARY:");
                break;
            case 2:
                System.out.println("YEAR OF THE MONTH FEBRUARY");
                break;
            case 3:
                System.out.println("YEAR OF THE MONTH MARCH");
                break;
            case 4:
                System.out.println("YEAR OF THE MONTH APRIL");
                break;
            case 5:
                System.out.println("YEAR OF THE MONTH MAY " );
                break;
            case 6:
                System.out.println("YEAR OF THE MONTH JUNE");
                break;
            case 7:
                System.out.println("YEAR OF THE MONTH JULY");
                break;
            case 8:
                System.out.println("YEAR OF THE MONTH AUGUST");
                break;
            case 9:
                System.out.println("YEAR OF THE MONTH SEPTEMBER");
                break;
            case 10:
                System.out.println("YEAR OF THE MONTH OCTOBER");
                break;
            case 11:
                System.out.println("YEAR OF THE MONTH NOVEMBER" );
                break;
            case 12:
                System.out.println("YEAR OF THE MONTH DECEMBER");
                break;
            default:
                System.out.println("INVALID MONTH OF THE YEAR");
                }

        }
    }

