package assignment_for_using_loops_day_9;

public class PrintOddNumber {

    public static void main(String[] args) {

        for (int i=50; i<=100; i++){

            if(i%2!=0){
                System.out.println("PRINT ODD NUMBER LIST: " +i);
            }
        }
    }
}
